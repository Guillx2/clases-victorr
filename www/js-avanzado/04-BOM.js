'use strict';

// BOM - Browser Object Model

/*
Con el BOM podemos ver propiedades de la ventana del navegador, 
como la altura, la anchura, etc. 
*/

//Para ver la altura del navegador:
console.log(window.innerHeight);

//Para ver la anchura del navegador:
console.log(window.innerWidth);

//Para ver las propiedades de la URL:
console.log(window.location);

//Para ver la URL:
console.log(window.location.href);

